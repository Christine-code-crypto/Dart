void main(List<String> args) {
  String weather = "Sunny";
  switch(weather){
    case "Sunny":
    print("The weather is sunny");
    break;
    case "Rainy":
    print("The weather is rainy");
    break;
    case "Windy":
    print("The weather is windy");
    break;
    default:
    print("Invalid weather condition");

  }
}
