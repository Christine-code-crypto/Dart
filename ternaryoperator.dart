void main() {
  int x = 10;
  var result = x % 2 == 1 ? "Even":"Odd";
  print(result);
}

/*
? "Even" : "Odd": The ternary conditional operator.
?: The conditional operator that starts the ternary expression.
"Even": The value assigned to result if x % 2 == 0 is true.
:: Separates the true value from the false value in the ternary expression.
"Odd": The value assigned to result if x % 2 == 0 is false.
*/
